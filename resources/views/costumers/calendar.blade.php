<script>
    let currentCalendarMonth = new Date();
    let bookedSlots = {};
    let selectedFilter = '';
    let region = '{{ $region ?? "padang" }}';

    document.addEventListener('DOMContentLoaded', () => {
        renderCalendar();
        loadBookedSlots();
    });

    function renderCalendar() {
        const year = currentCalendarMonth.getFullYear();
        const month = currentCalendarMonth.getMonth();
        const monthNames = ['Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni',
            'Juli', 'Agustus', 'September', 'Oktober', 'November', 'Desember'];

        document.getElementById('calendarMonth').textContent = `${monthNames[month]} ${year}`;

        const firstDay = new Date(year, month, 1);
        const lastDay = new Date(year, month + 1, 0);
        const daysInMonth = lastDay.getDate();
        const startingDayOfWeek = firstDay.getDay();

        let html = '';

        // Day headers
        const dayHeaders = ['Min', 'Sen', 'Sel', 'Rab', 'Kam', 'Jum', 'Sab'];
        dayHeaders.forEach(day => {
            html += `<div style="text-align: center; font-weight: 700; color: #667eea; padding: 10px 0; grid-column: span 1;">${day}</div>`;
        });

        // Empty cells
        for (let i = 0; i < startingDayOfWeek; i++) {
            html += '<div></div>';
        }

        // Days
        const today = new Date();
        for (let day = 1; day <= daysInMonth; day++) {
            const date = new Date(year, month, day);
            const dateStr = date.toISOString().split('T')[0];
            const dayKey = dateStr;

            let status = 'available';
            let bookingCount = 0;

            if (date < today) {
                status = 'unavailable';
            } else if (dayKey in bookedSlots) {
                status = 'booked';
                bookingCount = bookedSlots[dayKey].length;
            }

            html += `<div class="cal-day ${status}" onclick="showBookingDetails('${dayKey}')">
                <div class="day-number">${day}</div>
                <div class="day-status">${status === 'booked' ? `${bookingCount} booking` : status === 'available' ? '‚úì Tersedia' : '‚úó'}</div>
            </div>`;
        }

        document.getElementById('calendarGrid').innerHTML = html;
    }

    function loadBookedSlots() {
        const params = new URLSearchParams({
            tahun: currentCalendarMonth.getFullYear(),
            bulan: currentCalendarMonth.getMonth() + 1,
            region: region
        });

        if (selectedFilter) {
            params.append('lapangan_id', selectedFilter);
        }

        fetch(`{{ route('api.availability.booked-slots') }}?${params}`)
            .then(r => r.json())
            .then(data => {
                bookedSlots = {};
                data.forEach(slot => {
                    if (!bookedSlots[slot.tanggal]) {
                        bookedSlots[slot.tanggal] = [];
                    }
                    bookedSlots[slot.tanggal].push(slot);
                });
                renderCalendar();
            });
    }

    function showBookingDetails(date) {
        if (!bookedSlots[date]) return;

        let html = `<p><strong>Tanggal:</strong> ${new Date(date).toLocaleDateString('id-ID', {
            weekday: 'long',
            year: 'numeric',
            month: 'long',
            day: 'numeric'
        })}</p>`;

        bookedSlots[date].forEach(booking => {
            html += `
                <div class="booking-item">
                    <div><strong>Lapangan:</strong> ${booking.lapangan_nama}</div>
                    <div><strong>Pemesan:</strong> ${booking.nama_pemesan}</div>
                    <div><strong>Jam:</strong> ${booking.jam_mulai} - ${booking.jam_selesai}</div>
                </div>
            `;
        });

        document.getElementById('bookingDetails').innerHTML = html;
        document.getElementById('bookingModal').classList.add('active');
    }

    function closeModal() {
        document.getElementById('bookingModal').classList.remove('active');
    }

    function filterByLapangan() {
        selectedFilter = document.getElementById('filterLapangan').value;
        loadBookedSlots();
    }

    function prevMonth() {
        currentCalendarMonth = new Date(currentCalendarMonth.getFullYear(), currentCalendarMonth.getMonth() - 1);
        loadBookedSlots();
    }

    function nextMonth() {
        currentCalendarMonth = new Date(currentCalendarMonth.getFullYear(), currentCalendarMonth.getMonth() + 1);
        loadBookedSlots();
    }

    // Close modal when clicking outside
    window.onclick = function (event) {
        const modal = document.getElementById('bookingModal');
        if (event.target === modal) {
            modal.classList.remove('active');
        }
    }
</script>
